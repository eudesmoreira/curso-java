
public class Aluno {

	String nome;
	int idade;
	String documento;
	
	public String getNome() {
		return nome;
	}
	
	public int getIdade() {
		return idade;
	}
	
	public String getDocumento() {
		return documento;
	}
	
		
	public void setNome(String nome) {
        this.nome = nome;
    }
	
	public void setIdade(int idade) {
        this.idade = idade;
    }
	
	public void setDocumento(String documento) {
        this.documento = documento;
    }
	
}
